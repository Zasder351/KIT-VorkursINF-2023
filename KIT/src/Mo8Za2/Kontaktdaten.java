package Mo8Za2;

public class Kontaktdaten {

    String strasse = "";
    String hausnummer = "";
    String plz = "";
    String ort = "";
    String telefonnummer = "";
    String email = "";

    public void setStrasse(String newStrasse){
        strasse = newStrasse;
    }

    public String getStrasse(){
        return strasse;
    }

    public void setHausnummer(String newHausnummer){
        hausnummer = newHausnummer;
    }

    public String getHausnummer(){
        return hausnummer;
    }

    public void setPLZ(String newPLZ){
        plz = newPLZ;
    }

    public String getPlz(){
        return plz;
    }

    public void setOrt(String newOrt){
        ort = newOrt;
    }

    public String getOrt(){
        return ort;
    }

    public void setTelefonnummer(String newTelefonnummer){
        telefonnummer = newTelefonnummer;
    }

    public String getTelefonnummer(){
        return telefonnummer;
    }

    public void setEmail(String newEmail){
        email = newEmail;
    }

    public String getEmail(){
        return email;
    }

}
